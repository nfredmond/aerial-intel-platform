"use client";

import { useActionState, useState } from "react";
import { useFormStatus } from "react-dom";

import {
  generateMissionBriefAction,
  type MissionBriefFormState,
} from "@/app/missions/[missionId]/copilot-actions";
import { formatTenthCentsUsd } from "@/lib/copilot/pricing";

type Props = {
  missionId: string;
  missionName: string;
  /** True when both the env kill-switch and the per-org opt-in are on. */
  available: boolean;
  availabilityHint: string;
};

const INITIAL: MissionBriefFormState = { status: "idle" };

function SubmitButton({ disabled }: { disabled: boolean }) {
  const { pending } = useFormStatus();
  return (
    <button
      type="submit"
      disabled={disabled || pending}
      className="inline-flex items-center rounded-md bg-slate-900 px-3 py-1.5 text-sm font-medium text-white hover:bg-slate-700 disabled:cursor-not-allowed disabled:bg-slate-400"
    >
      {pending ? "Drafting brief…" : "Generate client brief"}
    </button>
  );
}

function blockedMessage(state: MissionBriefFormState): string | null {
  if (state.status !== "blocked") return null;
  switch (state.reason) {
    case "not-authenticated":
      return "Sign in is required to generate a copilot brief.";
    case "not-authorized":
      return "Your role does not include copilot.generate. Ask an admin.";
    case "global-disabled":
      return "Aerial Copilot is disabled on this deployment (env kill-switch).";
    case "missing-api-key":
      return "Aerial Copilot is missing its Anthropic API key. Check server config.";
    case "org-disabled":
      return "Aerial Copilot is off for this organization. An admin can enable it in org settings.";
    case "mission-not-found":
      return "This mission could not be loaded for your org.";
    case "no-facts":
      return "No citable facts were found for this mission yet. Complete ingest/processing first.";
    case "quota-exhausted": {
      const spent = formatTenthCentsUsd(state.spendTenthCents ?? 0);
      const cap = formatTenthCentsUsd(state.capTenthCents ?? 0);
      return `Monthly copilot quota exhausted (${spent} of ${cap} used). Wait for the month to reset or raise the cap.`;
    }
    default:
      return "Copilot call was blocked.";
  }
}

function refusedMessage(state: MissionBriefFormState): string | null {
  if (state.status !== "refused") return null;
  switch (state.reason) {
    case "too-many-dropped":
      return `Brief refused: ${state.droppedSentences} of ${state.totalSentences} sentences failed citation grounding. Try again or check the underlying data.`;
    case "too-short":
      return "Brief refused: the grounded output was too short to be useful.";
    case "empty-output":
      return "Brief refused: the model produced no citable sentences.";
  }
}

function BriefResult({
  state,
  missionName,
}: {
  state: MissionBriefFormState;
  missionName: string;
}) {
  const [copied, setCopied] = useState(false);
  if (state.status !== "ok") return null;

  const handleCopy = async () => {
    await navigator.clipboard.writeText(state.text);
    setCopied(true);
    setTimeout(() => setCopied(false), 1500);
  };

  const markdown = `# ${missionName} — Mission Brief\n\n${state.text}\n\n---\n*Generated by Aerial Copilot (${state.modelId}). ${state.keptSentences} of ${state.totalSentences} sentences kept after grounding.*\n`;

  const handleDownload = () => {
    const blob = new Blob([markdown], { type: "text/markdown" });
    const url = URL.createObjectURL(blob);
    const a = document.createElement("a");
    a.href = url;
    a.download = `${missionName.replace(/\s+/g, "_")}_brief.md`;
    document.body.appendChild(a);
    a.click();
    a.remove();
    URL.revokeObjectURL(url);
  };

  return (
    <div className="mt-3 space-y-3 rounded-md border border-slate-200 bg-slate-50 p-3">
      <p className="whitespace-pre-wrap text-sm text-slate-800">{state.text}</p>
      <div className="flex flex-wrap items-center gap-2 text-xs text-slate-600">
        <button
          type="button"
          onClick={handleCopy}
          className="rounded border border-slate-300 bg-white px-2 py-1 hover:bg-slate-100"
        >
          {copied ? "Copied" : "Copy"}
        </button>
        <button
          type="button"
          onClick={handleDownload}
          className="rounded border border-slate-300 bg-white px-2 py-1 hover:bg-slate-100"
        >
          Download .md
        </button>
        <span>
          {state.keptSentences}/{state.totalSentences} sentences kept · spend{" "}
          {formatTenthCentsUsd(state.spendTenthCents)} · model {state.modelId}
        </span>
      </div>
    </div>
  );
}

export function MissionBriefPanel({ missionId, missionName, available, availabilityHint }: Props) {
  const [state, formAction] = useActionState(generateMissionBriefAction, INITIAL);

  const blocked = blockedMessage(state);
  const refused = refusedMessage(state);

  return (
    <section className="rounded-md border border-slate-200 bg-white p-4 shadow-sm">
      <header className="mb-2 flex items-start justify-between gap-4">
        <div>
          <h3 className="text-sm font-semibold text-slate-900">Aerial Copilot — Mission Brief</h3>
          <p className="mt-0.5 text-xs text-slate-600">
            One-click ~250-word client brief. Every sentence cites a real record; ungrounded claims
            are dropped. AI-assisted draft — review before sharing externally.
          </p>
        </div>
      </header>

      {!available ? (
        <p className="rounded-md border border-amber-200 bg-amber-50 px-3 py-2 text-xs text-amber-900">
          {availabilityHint}
        </p>
      ) : (
        <form action={formAction} className="flex items-center gap-2">
          <input type="hidden" name="missionId" value={missionId} />
          <SubmitButton disabled={!available} />
          {state.status === "error" ? (
            <span className="text-xs text-rose-700">Error: {state.message}</span>
          ) : null}
        </form>
      )}

      {blocked ? (
        <p className="mt-3 rounded-md border border-amber-200 bg-amber-50 px-3 py-2 text-xs text-amber-900">
          {blocked}
        </p>
      ) : null}
      {refused ? (
        <p className="mt-3 rounded-md border border-rose-200 bg-rose-50 px-3 py-2 text-xs text-rose-900">
          {refused}
        </p>
      ) : null}

      <BriefResult state={state} missionName={missionName} />
    </section>
  );
}
